int	ft_atoi(const char *str)
{
	int	i;
	int	nb;
	int	si;

	i = 0;
	nb = 0;
	si = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			si = si * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb *= 10;
		nb += str[i] - '0';
		i++;
	}
	return (si * nb);
}
