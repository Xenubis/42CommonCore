#include "libft.h"

char	*ft_strchr(const char *str, char c)
{
	size_t	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			break ;
		i++;
	}
	if ((str[i] == c))
		return ((char *)str + i);
	return (NULL);
}
