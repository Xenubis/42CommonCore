int	ft_atoi(const char *str)
{
	int i;
	int si;
	int	res;
	
	i = 0;
	si = 1;
	res = 0;

	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
		i++;
	if (str[i] == '-')
	{
		si = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		res *= 10;
		res += str[i] - 48;
		i++;
	}
	return (res *= si);
}